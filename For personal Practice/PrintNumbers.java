class PrintingNumbers
{
	static void printNumbers()
	{
		int a = 0;
		while (a <= 10)
		{
			System.out.println("The 10 natural numbers are: ");
			
			a++;
		}
		
	}
	public static void main (String args[])
	{
		printNumbers();
	}
}